@hashes = {}
File.open './corncob_lowercase.txt', 'r' do |file|
  file.each_line do |line|
    line = line.strip
    if line.length <= 6
      digest = Digest::MD5.hexdigest(line)
      @hashes[digest] = line
    end
  end
end

